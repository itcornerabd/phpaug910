<?php 

	//for, while, do while
	
	// init , condition , increment 
	echo "for loop";
	for($i=11;$i<=10;$i++)
	{
		echo "<br>". $i;
	}
	$j=11;
	echo "while loop";
	while($j<=10)
	{
		echo "<br>".$j;
		$j++;
	}

	$k=11;
	echo "do while loop";
	do
	{
		echo "<br>".$k;
		$k++;
	}while($k<=10);
	



	
	


 ?>